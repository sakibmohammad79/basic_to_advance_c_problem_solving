#include <stdio.h>
#include <stdlib.h>  // abs ফাংশনটি ব্যবহার করতে

int main() {
    int matrix[5][5];  // 5x5 ম্যাট্রিক্সটি সেভ করতে
    int row, col;      // 1-এর অবস্থান সেভ করার জন্য0 0 0 0 0

    // Step 1: ইনপুট নেওয়া
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            scanf("%d", &matrix[i][j]);  // ম্যাট্রিক্সের সব উপাদান ইনপুট করা
            if (matrix[i][j] == 1) {  // যদি 1 পাওয়া যায়
                row = i;   // 1-এর সারি সেভ করা
                col = j;   // 1-এর কলাম সেভ করা
            }
        }
    }

    // Step 2: 1 কে (2, 2) তে নিয়ে আসার জন্য পদক্ষেপ গননা
    // 1 কে (2, 2) (মাঝের সারি, কলাম) তে নিয়ে আসতে কতটুকু পদক্ষেপ নিতে হবে:
    int moves = abs(row - 2) + abs(col - 2);  // abs দিয়ে দূরত্ব ক্যালকুলেট করা

    // ফলাফল প্রিন্ট করা
    printf("%d\n", moves);  // মোট পদক্ষেপের সংখ্যা প্রিন্ট করা
    return 0;
}
